// comparison Operators
// Relational OP
console.log('Comparison Operators');
let z = 2;
console.log('Relational Operators');
console.log(z > 0);
console.log(z >= 1);
console.log(z < 1);
console.log(z <= 1);

//Equality
console.log('Equality Operators');
console.log(z === 2);
console.log(z !== 2);


// Equality Operators
console.log('Strict & loose Operators');
// Strict (Type & value)
console.log('2' === 2);
console.log(2 === 2);

// Lose equality (only value)
console.log('2' == 2);
console.log(2 == 2);
console.log(true == 1);