// Arithmetic Operators
let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x / y);
console.log(x % y);
console.log(x ** y); // Expenential operator

// Increment(++)
//console.log(++x);
console.log(x++);
console.log(x);

// Decrement(--)
//console.log(--x);
console.log(x--);
console.log(x);

// Assignment Operators
//let x = 10;

x++;
